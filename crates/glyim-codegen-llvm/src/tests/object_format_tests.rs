#[test]
fn s08_t23_generate_function_bytes_consistent() {
    let backend = LlvmBackend::new();
    let body = std::sync::Arc::new(glyim_mir::Body::dummy(glyim_core::DefId::new(
        glyim_core::CrateId::from_raw(0),
        glyim_core::LocalDefId::from_raw(102),
    )));
    let r1 = backend
        .generate_function(&body)
        .expect("first call should succeed");
    let r2 = backend
        .generate_function(&body)
        .expect("second call should succeed");
    // With identical input and same context, the generated bytes should be identical
    // (deterministic codegen).
    assert_eq!(
        r1, r2,
        "generate_function should be deterministic for identical input"
    );
}

