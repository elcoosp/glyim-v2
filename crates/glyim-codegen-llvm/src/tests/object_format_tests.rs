#[test]
fn s08_t25_generate_after_many_functions() {
    let backend = LlvmBackend::new();
    // Call generate_function many times first
    for i in 0..50 {
        let body = std::sync::Arc::new(glyim_mir::Body::dummy(glyim_core::DefId::new(
            glyim_core::CrateId::from_raw(0),
            glyim_core::LocalDefId::from_raw(i as u32),
        )));
        backend.generate_function(&body).unwrap();
    }
    // Then call generate with a batch
    let output = Path::new("/tmp/glyim_test_after_functions.o");
    let bodies: Vec<_> = (0..10)
        .map(|i| {
            std::sync::Arc::new(glyim_mir::Body::dummy(glyim_core::DefId::new(
                glyim_core::CrateId::from_raw(0),
                glyim_core::LocalDefId::from_raw(1000 + i as u32),
            )))
        })
        .collect();
    let result = backend.generate(&bodies, output);
    assert!(
        result.is_ok(),
        "generate after many generate_function calls should succeed"
    );
    assert!(output.exists());
    std::fs::remove_file(output).ok();
}

#[test]
fn s08_t26_name_is_static() {
    let b1 = LlvmBackend::new();
    let b2 = LlvmBackend::new();
    let name1 = b1.name();
    let name2 = b2.name();
    assert_eq!(name1, name2);
    // The string slice should be static (same pointer across instances)
    assert!(
        std::ptr::eq(name1, name2),
        "name() should return a static string slice"
    );
}
